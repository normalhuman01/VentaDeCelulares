    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel _tablaProductos = (DefaultTableModel) Vistas.formListaCelulares.jTable1.getModel();
        boolean Identicos = false;
        if(txtClasificacion.getText().isEmpty() || txtNombre.getText().isEmpty() || ftxtID.getText().isEmpty() || ftxtPrecio.getText().isEmpty() || ftxtStock.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Favor de llenar todos los campos", "Mensae", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            
            
            if(!_listaCelular.isEmpty()){
                for(int i = 0; i < _listaCelular.size();i++){
                    
                    if(!(_listaCelular.get(i).getID().equals(ftxtID.getText()) || _listaCelular.get(i).getNombreProducto().toUpperCase().equals(txtNombre.getText().toUpperCase()))){
                        Identicos = true;   //use una variable booleana por va el ciclo recorriendo del 0 ansta que se acabe el agreglo pero si agregas uno y el primero es diferente que el segunto lo agrega pero al final te sale que no hay repetidos
                    
                    }
                    
                    else{
                    JOptionPane.showMessageDialog(null, "Ya hay un cliente con el mismo ID o nombre", "Aviso", JOptionPane.PLAIN_MESSAGE);
                        Identicos = false;
                    break;
                
                    }
                    
                
                }
                if(Identicos == true){
                        
                        
                        _listaCelular.add(new Celulares(ftxtID.getText(), txtNombre.getText(), Double.parseDouble(ftxtPrecio.getText()), Integer.parseInt(ftxtStock.getText()), txtClasificacion.getText()));
                        _tablaProductos.addRow(new Object[]{ftxtID.getText(),txtNombre.getText(),ftxtPrecio.getText(),ftxtStock.getText(),txtClasificacion.getText()});
                        Vistas.formListaCelulares.jTable1.setModel(_tablaProductos);
                        JOptionPane.showMessageDialog(null, "Se a agregado un nuevo producto","Aviso"  , JOptionPane.INFORMATION_MESSAGE);
                        hide();
                        txtNombre.setText("");
                        txtClasificacion.setText("");
                        ftxtID.setText("");
                        ftxtPrecio.setText("");
                        ftxtStock.setText("");
                        Identicos = false;
                      
                        
                    }
            
            }
            
            if(_listaCelular.isEmpty()){
                
                
                _listaCelular.add(new Celulares(ftxtID.getText(), txtNombre.getText(), Double.parseDouble(ftxtPrecio.getText()), Integer.parseInt(ftxtStock.getText()), txtClasificacion.getText()));
                _tablaProductos.addRow(new Object[]{ftxtID.getText(),txtNombre.getText(),ftxtPrecio.getText(),ftxtStock.getText(),txtClasificacion.getText()});
                Vistas.formListaCelulares.jTable1.setModel(_tablaProductos);
                JOptionPane.showMessageDialog(null, "Se a agregado un nuevo producto","Aviso"  , JOptionPane.INFORMATION_MESSAGE);
                hide();
                txtNombre.setText("");
                txtClasificacion.setText("");
                ftxtID.setText("");
                ftxtPrecio.setText("");
                ftxtStock.setText("");
            }
            //
            
            
        }
        System.out.println(""+_listaCelular.size());
     
    }//GEN-LAST:event_jButton1ActionPerformed

