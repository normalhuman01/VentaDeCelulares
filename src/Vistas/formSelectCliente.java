    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        jComboBox2.setVisible(true);
        _cbox = (DefaultComboBoxModel) jComboBox2.getModel();
        try {
            _cbox.removeAllElements();
            int Eleccion = jComboBox1.getSelectedIndex();
            if(Eleccion == 0){
                formCliente._clientesLista.sort(Comparator.comparing(modelo.Clientes::getID));
                for(modelo.Clientes _client : formCliente._clientesLista){
                    
                    _cbox.addElement(_client.getID());
                }
        
                
            }
            if(Eleccion == 1){
                formCliente._clientesLista.sort(Comparator.comparing(modelo.Clientes::getNombre));
                for(modelo.Clientes _client : formCliente._clientesLista){
                    _cbox.addElement(_client.getNombre());
                }
            }
           formSelectCelular.cbProductos.setModel(_cbox);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

