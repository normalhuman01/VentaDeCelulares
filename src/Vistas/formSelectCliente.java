    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        formVerificacionClientes _verificar = new formVerificacionClientes();
        formVentaCelulares.dskEscritorio.add(_verificar);
        int x = (int) (formVentaCelulares.dskEscritorio.getWidth() / 2) - (int) (_verificar.getWidth() / 2);
        int y = (int) (formVentaCelulares.dskEscritorio.getHeight() / 2) - (int) (_verificar.getHeight() / 2);

        _verificar.setLocation(x, y);
        _verificar.setVisible(true);
        for(int i =0; i < formCliente._clientesLista.size(); i++){
            if(formCliente._clientesLista.get(i).getNombre().equals(jComboBox2.getSelectedItem()+"")|| formCliente._clientesLista.get(i).getID().equals(jComboBox2.getSelectedItem()+"")){
                formVerificacionClientes.jTextField1.setText(formCliente._clientesLista.get(i).getID());
                formVerificacionClientes.jTextField2.setText(formCliente._clientesLista.get(i).getNombre());
                formVerificacionClientes.jTextField3.setText(formCliente._clientesLista.get(i).getDomicilio());
                formVerificacionClientes.jTextField4.setText(formCliente._clientesLista.get(i).getRFC());
                formVerificacionClientes.jTextField5.setText(formCliente._clientesLista.get(i).getTelefono());
                formVerificacionClientes.jTextField6.setText(formCliente._clientesLista.get(i).getCorreoElectronico());
                
                break;
            }
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

